// list comes from each tool's seo block. Keeping it there avoids pulling
// the SEO-content map into every page that imports this registry (e.g.
// the client search index).

export function countToolsInCategory(categoryId: string): {
  total: number;
  live: number;
} {
  const all = getToolsByCategory(categoryId);
  return {
    total: all.length,
    live: all.filter((t) => t.status === "live").length,
  };
}
